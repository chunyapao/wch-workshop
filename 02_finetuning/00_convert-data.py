def convert_to_isan_finetuning():
    source_files = [
        "../01_pretraining/data/raw/train.jsonl",
        "../01_pretraining/data/raw/valid.jsonl"
    ]

    # 1. โหลดข้อมูลดิบจาก Pretraining
    all_texts = []
    for file_path in source_files:
        with open(file_path, "r", encoding="utf-8") as f:
            for line in f:
                data = json.loads(line)
                if data.get("text", "").strip():
                    all_texts.append(data["text"].strip())

    print(f"พบข้อมูลทั้งหมด {len(all_texts)} รายการ")

    # 2. สร้างพจนานุกรม + Pattern สำหรับ re.sub() (เร็วกว่าวน .replace() หลายเท่า)
    thai_to_isan = build_thai_to_isan_dict()

    # เรียงคำยาว → สั้น เพื่อป้องกันแทนที่ซ้อนทับ
    # เช่น "พวกเรา" ต้องถูกแทนที่ก่อน "เรา"
    sorted_keys = sorted(thai_to_isan.keys(), key=len, reverse=True)
    pattern = re.compile("|".join(re.escape(k) for k in sorted_keys))

    def translate(text):
        return pattern.sub(lambda m: thai_to_isan[m.group(0)], text)

    # 3. แปลงข้อมูลเป็นรูปแบบ Prompt-Completion
    formatted_data = []
    skipped = 0

    for text in all_texts:
        isan_text = translate(text)

        # ข้ามถ้าแปลไม่เปลี่ยนอะไรเลย (ไม่มีคำอีสานในพจนานุกรม)
        if isan_text == text:
            skipped += 1
            continue

        # สุ่มประเภทงาน
        task_type = random.choice(["th_to_is", "question"])

        if task_type == "th_to_is":
            prompt = f"จงแปลประโยคต่อไปนี้เป็นภาษาอีสาน: {text}"
            completion = isan_text
        else:
            prompt = f"อธิบายเกี่ยวกับเรื่องนี้ในภาษาอีสาน: {text}"
            completion = isan_text

        formatted_data.append({"prompt": prompt, "completion": completion})

    print(f"สร้างข้อมูลได้ {len(formatted_data)} รายการ (ข้าม {skipped} รายการที่แปลไม่เปลี่ยน)")

    # 4. แบ่งข้อมูล 90% train, 10% valid
    random.shuffle(formatted_data)
    train_data = formatted_data[:int(len(formatted_data) * 0.9)]
    valid_data = formatted_data[int(len(formatted_data) * 0.9):]

    # 5. บันทึก (ลง data/raw/ สำหรับเทรนรอบที่ 1 - เรียนรู้ภาษาอีสาน)
    target_dir = "./data/raw"
    os.makedirs(target_dir, exist_ok=True)

    def save_jsonl(data, filepath):
        with open(filepath, "w", encoding="utf-8") as f:
            for item in data:
                f.write(json.dumps(item, ensure_ascii=False) + "\n")

    save_jsonl(train_data, os.path.join(target_dir, "train.jsonl"))
    save_jsonl(valid_data, os.path.join(target_dir, "valid.jsonl"))

    print(f"\nบันทึกข้อมูล train: {len(train_data)} รายการ")
    print(f"บันทึกข้อมูล valid: {len(valid_data)} รายการ")


if __name__ == "__main__":
    convert_to_isan_finetuning()
